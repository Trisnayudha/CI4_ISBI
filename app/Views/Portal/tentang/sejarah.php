    <p style="text-align: justify; font-size: 17px; text-indent: 50px; font-family: 'Arvo', serif">
      Berawal dari aspirasi masyarakat Jawa Barat yang menghendaki adanya lembaga pendidikan tinggi seni tari di Bandung, dengan melalui Surat Keputusan Walikota-madya Bandung nomor 5539/68, tanggal 31 Maret 1968 di Bandung didirikan
      Konservatori Tari (KORI) yang pengelolaannya ada di bawah Pemerintah Daerah Tingkat II Kotamadya Bandung.
    </p>
    <p style="text-align: justify; font-size: 17px; text-indent: 50px; font-family: 'Arvo', serif">
      Atas meningkatnya animo masyarakat dan besarnya perhatian pemerintah, maka KORI ber-upaya agar keberadaannya dapat diakui sebagai lembaga formal. Dengan adanya kesepakatan antara Dirjen Kebudayaan Kantor Daerah Kodya Bandung,
      Pemerintah Kodya Bandung, Inspektorat Pendidikan Kesenian Jawa Barat, dan Direktur Akademi Seni Tari Indonesia di Yogyakarta, lahirlah Surat Keputusan Menteri Pendidikan dan Kebudayaan Republik Indonesia nomor 016/A.I/1970 tentang
      Akademi Seni Tari Indonesia (ASTI) Bandung yang merupakan kelas jauh ASTI Yogyakarta. Dengan demikian, sejak tanggal 27 Februari 1971, Konservatori Tari berubah menjadi Akademi Seni Tari Indonesia Jurusan Sunda di Bandung.
    </p>
    <p style="text-align: justify; font-size: 17px; text-indent: 50px; font-family: 'Arvo', serif">
      Pada tahun 1976 ASTI Jurusan Sunda di Bandung berada dalam pembinaan Direktorat Jenderal Kebudayaan Depdikbud bersama dengan perguruan tinggi lainnya, yaitu Akademi Seni Rupa Indonesia (ASRI) Yogyakarta, Akademi Seni Tari
      Indonesia (ASTI) Yogyakarta, Akademi Seni Karawitan Indonesia (ASKI) Padang panjang, dan Akademi Seni Tari Indonesia (ASTI) Denpasar. Semuanya dihimpun dalam satu proyek, yaitu Proyek Pengembangan Institut Kesenian Indonesia (IKI)
      Jakarta. Di antara perguruan tinggi tersebut, yaitu ASTI Yogyakarta, ASKI Surakarta, dan ASTI Denpasar, statusnya telah ditingkatkan. ASTI dan akademi-akademi lainnya yang ada di Yogyakarta dilebur dan kemudian ditingkatkan
      statusnya menjadi Institut Seni Indonesia (ISI), ASKI Surakarta menjadi Sekolah Tinggi Seni Indonesia (STSI), ASTI Denpasar menjadi Sekolah Tinggi Seni Indonesia (STSI), dan ASTI Bandung mendapat giliran menjadi Sekolah Tinggi
      Seni Indonesia (STSI) melalui Surat Keputusan Presiden RI No. 59 Tahun 1995. STSI Bandung terdiri dari jurusan Tari, Karawitan, Teater, dan Seni Rupa Pertunjukan. Pada 13 Januari 2011 terbentuklah Program Studi Senirupa Murni
      jenjang Diploma III (D-3), Kemudian menyusul Prodi Rias dan Busana jenjang Diploma IV (D-4) berdasarkan Keputusan Menteri Pendidikan dan Kebudayaan Nomor : 141/E/O/2012 tanggal 4 April 2012 tentang penyelenggaraan program Studi
      Televisi dan Film jenjang Diploma IV (D-4) pada STSI Bandung, serta Program Studi Angklung dan Musik Bambu Jenjang Diploma IV (D-4), berdasarkan Kemendikbud Republik Indonesia, Nomor : 149/E/O/2012, tanggal 27 April 2012.
      Selanjutnya pada tahun Akademik 2012/2013 secara resmi menerima mahasiswa baru angkatan pertama.
    </p>
    <p style="text-align: justify; font-size: 17px; text-indent: 50px; font-family: 'Arvo', serif">
      Beralihnya status STSI menjadi ISBI adalah bagian dari Masterplan Percepatan dan Perluasan Pembangunan (MP3EI) untuk meningkatkan akses masyarakat terhadap pendidikan tinggi, sekaligus meningkatkan kualitas pendidikan khususnya
      Seni Budaya. Perubahan Sekolah Tinggi Seni Indonesia (STSI) Bandung menjadi Institut Seni Budaya Indonesia (ISBI) Bandung sesuai dengan Perpres No. 86 Tahun 2014 tanggal 25 Agustus 2014, ditandatangan dan diresmikan oleh Presiden
      Susilo Bambang Yudhoyono tanggal 6 Oktober 2014 pukul 14.32 WIB bertempat di Gedung Nala, Markas Komando Armada RI Kawasan Timur (MAKO ARMATIM), Surabaya. Tak hanya ISBI Bandung yang diresmikan tetapi juga Institut Seni dan Budaya
      Indonesia Tanah Papua dan Institut Seni dan Budaya Indonesia Aceh
    </p>
